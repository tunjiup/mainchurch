function k1deno()
{
	if(document.getElementById('1kchk').checked==true)
		{
            document.getElementById('1kqty').disabled =false;
            document.getElementById('1kttl').readonly =false;
        }
    else if(document.getElementById('1kchk').checked==false)
		{
            document.getElementById('1kqty').value ="";
            document.getElementById('1kttl').value ="";
            document.getElementById('1kqty').disabled =true;
            document.getElementById('1kttl').disabled =true;
           
        }	
}

